#include "libft.h"

char	*ft_strcat(char *s1, const char *s2)
{
	int	i;
	int	j;

	i = -1;
	j = 0;
	while (s1[j])
		j++;
	while (s2[++i])
	{
		if (s2[i])
			s1[j++] = s2[i];
	}
	s1[j] = '\0';
	return (s1);
}
